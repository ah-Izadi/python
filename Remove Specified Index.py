thislist = ["apple", "banana", "cherry"]
thislist.pop(1)
print(thislist)

#pop() method can remove the last item in list

thislist = ["apple", "banana", "cherry"]
thislist.pop()
print(thislist)

#del method also like pop() method
thislist = ["apple", "banana", "cherry"]
del thislist[0]
print(thislist)

#del method also can delete the list completely
thislist = ["apple", "banana", "cherry"]
del thislist